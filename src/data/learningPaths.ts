export function getLearningPath(role: Role, phase?: Phase): LearningUnit[] {
  return LEARNING_UNITS.filter(unit => {
    const phaseMatch = phase ? unit.phase === phase : true;
    const roleMatch = unit.roles.length === 0 || unit.roles.includes(role);
    return phaseMatch && roleMatch;
  });
}

export function getPhaseInfo(phase: Phase): PhaseInfo {
  return PHASES[phase];
}

export function getAllPhases(): PhaseInfo[] {
  return Object.values(PHASES);
}

export function getAllRoles(): RoleInfo[] {
  return Object.values(ROLES);
}
